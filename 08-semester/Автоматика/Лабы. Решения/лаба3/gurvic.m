% Критерий Гурвица для замкнутой системы
% Вариант 12: W(s) = 1/(0.05s^4 + 0.1s^3 + s^2 + s + 1)

clear; clc;

%% Характеристический полином замкнутой системы
% D(s) = 0.05s^4 + 0.1s^3 + s^2 + s + 2
a = [0.05, 0.1, 1, 1, 2];  % коэффициенты от a0 до a4

n = length(a) - 1;  % степень полинома (n=4)

fprintf('Характеристический полином замкнутой системы:\n');
fprintf('%.2f s^%d + %.2f s^%d + %.2f s^%d + %.2f s + %.2f\n\n', ...
    a(1), n, a(2), n-1, a(3), n-2, a(4), a(5));

%% Построение матрицы Гурвица
H = zeros(n, n);

for i = 1:n
    for j = 1:n
        idx = 2*i - j;
        if idx >= 0 && idx <= n
            H(i, j) = a(idx + 1);
        else
            H(i, j) = 0;
        end
    end
end

fprintf('Матрица Гурвица H (%dx%d):\n', n, n);
disp(H);

%% Вычисление диагональных определителей (миноров Гурвица)
dets = zeros(1, n);

for i = 1:n
    dets(i) = det(H(1:i, 1:i));
    fprintf('Δ%d = %.4f\n', i, dets(i));
end

fprintf('\n');

%% Проверка условий устойчивости
% Необходимое условие: все коэффициенты a_i > 0
if all(a > 0)
    fprintf('Необходимое условие выполняется: все коэффициенты > 0\n');
else
    fprintf('Необходимое условие НЕ выполняется: есть отрицательные коэффициенты\n');
end

% Достаточное условие: все миноры Гурвица > 0
if all(dets > 0)
    fprintf('Все миноры Гурвица положительны.\n');
    fprintf('ВЫВОД: Замкнутая система УСТОЙЧИВА по критерию Гурвица.\n');
else
    fprintf('Не все миноры Гурвица положительны.\n');
    fprintf('ВЫВОД: Замкнутая система НЕУСТОЙЧИВА.\n');
end