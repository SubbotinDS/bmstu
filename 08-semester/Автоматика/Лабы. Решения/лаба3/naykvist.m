clc, clear, close all

% Передаточная функция разомкнутой системы
w = tf([1], [0.05, 0.1, 1, 1, 1]);

% Переходная функция разомкнутой системы
figure;
step(w);
title('Переходная функция разомкнутой системы');
grid on;

% Проверка устойчивости
fprintf('Полюса разомкнутой системы:\n');
disp(pole(w));

if isstable(w)
    fprintf('Разомкнутая система УСТОЙЧИВА\n');
else
    fprintf('Разомкнутая система НЕУСТОЙЧИВА\n');
end